//Autoboxing greatly simplifies and streamlines the code that should convert primitive types to objects, and vice versa
//It's the process by which a primitive type is automatically boxed in its equivalent type wrapper when an object of that type is needed.
class Autobox {
    public static void main(String[] args) {
        Integer a = 100;
        int i=a;
        System.out.println(i+ " "+a);
    }
}